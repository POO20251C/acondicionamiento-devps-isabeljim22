# Solucion punto 1
def sumaDigitos(): # Dado un número entero, calcula la suma de todos sus dígitos.

    numero = int(input("Ingrese un número: "))
    suma = 0
    while numero > 0:
        suma += numero % 10 #
        numero //= 10
    print("La suma de los dígitos es:", suma)
    print("\n")

